import turtle  # 2D GRAPHICS MODULE

cam_turtle = turtle.Turtle()  # Initializing the turtle
screen = turtle.Screen()  # For Window

# Commands for the turtle
cam_turtle.forward(200)
cam_turtle.right(90)
cam_turtle.forward(200)
cam_turtle.right(90)
cam_turtle.forward(200)
cam_turtle.right(90)
cam_turtle.forward(200)
screen.exitonclick()  # Closing operation
